        private void carregarTabelaEditoras() {
        String[] colunas = {"ID", "Nome", "Gerente", "Endere√ßo", "Telefone"};
        
        DefaultTableModel model = new DefaultTableModel(colunas, 0);
        
        List<Editora> listaDeEditoras = editoraControle.listarTodas();
        
        for (Editora editora : listaDeEditoras) {
            Object[] linha = {
                editora.getID(),
                editora.getNome(),
                editora.getGerente(),
                editora.getEndereco(),
                editora.getTelefone()
            };
            model.addRow(linha);
        }
        
        tabelaEditoras.setModel(model); 
    }
